// Type for the common translations structure
export type CommonTranslations = typeof commonTranslations

// Helper function to get a specific translation value with fallback
export function getTranslation(
  lang: keyof CommonTranslations,
  key: keyof CommonTranslations['en']
): string {
  return (commonTranslations[lang]?.[key] ?? commonTranslations.en?.[key] ?? key) as string
}